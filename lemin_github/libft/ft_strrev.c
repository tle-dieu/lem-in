#include "libft.h"

char	*ft_strrev(char *str)
{
	size_t j;
	size_t i;

	i = 0;
	j = ft_strlen(str);
	while (i < j)
	{
		ft_swap(&str[i], &str[j]);
		i++;
		j--;
	}
	return (str);
}
