#include <stdio.h>

char	*ft_strupper(char *str)
{
	int	i;

	i = 0;
	printf("str = %s\n", str);
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= 32;
		i++;
	}
	return (str);
}
