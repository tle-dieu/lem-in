char	*ft_strrev(char *str)
{
	int		j;
	int		i;
	char	temp;

	j = 0;
	i = 0;
	while (str[j])
		j++;
	while (i < --j)
	{
		temp = str[j];
		str[j] = str[i];
		str[i] = temp;
		i++;
	}
	return (str);
}
